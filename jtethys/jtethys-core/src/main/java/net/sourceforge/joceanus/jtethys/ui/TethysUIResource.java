package net.sourceforge.joceanus.jtethys.ui;

import net.sourceforge.joceanus.jtethys.OceanusException;
import net.sourceforge.joceanus.jtethys.resource.TethysResourceBuilder;
import net.sourceforge.joceanus.jtethys.resource.TethysResourceId;

/**
 * Resource IDs for TethysUI package.
 */
public enum TethysUIResource implements TethysResourceId {
    /**
     * Bad value on parse.
     */
    PARSE_BADVALUE("parse.BadValue"),

    /**
     * AboutBox version.
     */
    ABOUT_VERSION("about.Version"),

    /**
     * AboutBox Revision.
     */
    ABOUT_REVISION("about.Revision"),

    /**
     * AboutBox BuilyOn.
     */
    ABOUT_BUILTON("about.BuiltOn"),

    /**
     * About OK.
     */
    ABOUT_OK("about.OK");

    /**
     * The Resource Builder.
     */
    private static final TethysResourceBuilder BUILDER = TethysResourceBuilder.getPackageResourceBuilder(OceanusException.class.getCanonicalName());

    /**
     * The Id.
     */
    private final String theKeyName;

    /**
     * The Value.
     */
    private String theValue;

    /**
     * Constructor.
     * @param pKeyName the key name
     */
    TethysUIResource(final String pKeyName) {
        theKeyName = pKeyName;
    }

    @Override
    public String getKeyName() {
        return theKeyName;
    }

    @Override
    public String getNameSpace() {
        return "ui";
    }

    @Override
    public String getValue() {
        /* If we have not initialised the value */
        if (theValue == null) {
            /* Derive the value */
            theValue = BUILDER.getValue(this);
        }

        /* return the value */
        return theValue;
    }
}
